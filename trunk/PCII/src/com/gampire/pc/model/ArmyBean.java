package com.gampire.pc.model;

public class ArmyBean {

	private String name;

	private String alliance;

	private String flag;

	private int colorRedValue;

	private int colorGreenValue;

	private int colorBlueValue;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getAllianceName() {
		return alliance;
	}

	public void setAllianceName(String allianceName) {
		this.alliance = allianceName;
	}

	public String getFlag() {
		return flag;
	}

	public void setFlag(String flag) {
		this.flag = flag;
	}

	public int getColorRedValue() {
		return colorRedValue;
	}

	public void setColorRedValue(int colorRedValue) {
		this.colorRedValue = colorRedValue;
	}

	public int getColorGreenValue() {
		return colorGreenValue;
	}

	public void setColorGreenValue(int colorGreenValue) {
		this.colorGreenValue = colorGreenValue;
	}

	public int getColorBlueValue() {
		return colorBlueValue;
	}

	public void setColorBlueValue(int colorBlueValue) {
		this.colorBlueValue = colorBlueValue;
	}

}
